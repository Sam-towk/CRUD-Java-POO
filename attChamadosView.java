    private void jBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSalvarActionPerformed
        String usuario = jTxtUsuario.getText();
        String ticket = jTxtTicket.getText();
        String setor = jTxtSetor.getText();
        String problema = jTxtProblema.getText();
        double custo = jTxtCusto.getText().isEmpty() ? 0 : Double.parseDouble(jTxtCusto.getText());
        boolean resolvido = jCbxResolvido.getSelectedItem().equals("Sim");

        if(usuario.isEmpty() || ticket.isEmpty() || setor.isEmpty() || problema.isEmpty()){
          JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios!");
            return;
        }

        attChamadosModel chamado = new attChamadosModel(usuario, ticket, setor, problema, custo, resolvido);
        attChamadosController ctrl = new attChamadosController();
        ctrl.salvar(chamado);
        atualizarCampos();
        atualizarTabela("");
          
    }//GEN-LAST:event_jBSalvarActionPerformed

    private void jBAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAtualizarActionPerformed
        int linhaSelecionada = jTChamados.getSelectedRow();
        if(linhaSelecionada >=0){
            String usuario = jTxtUsuario.getText();
            String ticket = jTxtTicket.getText();
            String setor = jTxtSetor.getText();
            String problema = jTxtProblema.getText();
            double custo = jTxtCusto.getText().isEmpty() ? 0 : Double.parseDouble(jTxtCusto.getText());
            boolean resolvido = jCbxResolvido.getSelectedItem().equals("Sim");

            if(usuario.isEmpty() || ticket.isEmpty() || setor.isEmpty() || problema.isEmpty()){
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios!");
                return;
            }

            attChamadosModel chamadoAtualizado = new attChamadosModel(usuario, ticket, setor, problema, custo, resolvido);
            attChamadosController ctrl = new attChamadosController();
            ctrl.atualizar(linhaSelecionada, chamadoAtualizado);
            atualizarCampos();
            atualizarTabela("");

        } 
        
    }//GEN-LAST:event_jBAtualizarActionPerformed

    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        int linhaSelecionada = jTChamados.getSelectedRow();
        if(linhaSelecionada >=0){
            attChamadosController ctrl = new attChamadosController();
            ctrl.excluir(linhaSelecionada);
            atualizarCampos();
            atualizarTabela("");
        }else {
            JOptionPane.showMessageDialog(null,"");
        }
        
    }//GEN-LAST:event_jBExcluirActionPerformed

