    private void jTChamadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTChamadosMouseClicked
         int linhaSelecionada = jTChamados.getSelectedRow();
        if(linhaSelecionada >=0){
            attChamadosController ctrl = new attChamadosController();
            
            attChamadosModel chamadoSelecionado = ctrl.listar().get(linhaSelecionada);jTxtUsuario.setText("");
            jTxtUsuario.setText(chamadoSelecionado.getUsuario());
            jTxtTicket.setText(chamadoSelecionado.getTicket());
            jTxtSetor.setText(chamadoSelecionado.getSetor());
            jTxtProblema.setText(chamadoSelecionado.getProblema());
            if(chamadoSelecionado.isResolvido()){
                jCbxResolvido.setSelectedItem("Sim");
            } else {
                jCbxResolvido.setSelectedItem("Não");
            }
            jTxtCusto.setText(String.valueOf(chamadoSelecionado.getCusto()));
            
            
            
            
        }else {
            JOptionPane.showMessageDialog(null,"");
        }
    }//GEN-LAST:event_jTChamadosMouseClicked

